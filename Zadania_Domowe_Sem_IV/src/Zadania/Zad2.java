package Zadania;

public class Zad2 {
    public static void main(String[] args){
        System.out.println("Zadania z programowania1. ");
        System.out.println("Zadania z programowania2. ".charAt(0));
        System.out.println("Zadania z programowania3. ".length());
        System.out.println("Zadania z programowania4. ".charAt(23));
        System.out.println("Zadania z programowania5. ".toUpperCase());
        System.out.println("Zadania z programowania6. ".toLowerCase());
        System.out.println("Zadania z programowania7. ".indexOf('z'));
        System.out.println("Zadania z programowania8. ".indexOf("prog"));
        System.out.println("Zadania z programowania9. ".replace('.','?'));
        System.out.println("Zadania z programowania10. ".replace("adania","dania"));
        System.out.println("Zadania z programowania11. ".replaceAll("ania","anka"));
        System.out.println("Zadania z programowania12. ".replaceFirst("ania","anka"));
        System.out.println("Zadania z programowania13. ".substring(10));
        System.out.println("Zadania z programowania14. ".substring(10,17));
        System.out.println("Zadania z programowania15. ".concat("\b z podpowiedziami."));
        System.out.println("Zadania z programowania16. " + "\b" + " z odpowiedziami.");
    }
}
